function gray_opaque_colormap(){

  var colormap=[];

  var N=256;
  for (var I=0;I<N;I++)
  {
    var alpha=I/(N-1.0);

    colormap.push(alpha*255);
    colormap.push(alpha*255);
    colormap.push(alpha*255);
    colormap.push(1.0*255);
  }

  //console.log(colormap);

  return colormap;

}

function gray_transparent_colormap(){

  var colormap=[];

  var N=256;
  for (var I=0;I<N;I++)
  {
    var alpha=I/(N-1.0);

    colormap.push(alpha*255);
    colormap.push(alpha*255);
    colormap.push(alpha*255);
    colormap.push(alpha*alpha*255);
  }

  //console.log(colormap);

  return colormap;

}

function hsl_colormap(){
  var colormap=[];
  var N=256;
  for (var I=0;I<N;I++)
  {
    var alpha=I/(N-1.0);
    colormap.push(0.5*255);
    colormap.push(1.0*255);
    colormap.push(1.0*255);
    colormap.push(alpha*255);
  }

  return colormap;
}